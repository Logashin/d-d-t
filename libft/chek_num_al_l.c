#include "libft.h"

int		chek_num_al_l(char *str)
{
	int i;
	
	i = 0;
	if (str[i] == '\0' || str[i] == '\n')
		return (0);
	while (str[i])
	{
		if (str[i] != 'L')
			i++;
		else
	    	return (0);
	}
	return (1);
}
